fun symbolToIcon(symbol: String?): ImageVector? {
    if (symbol == null) return null
    allIconsByName[symbol]?.let { return it }
    sfSymbolMapping[symbol]?.let { mapped -> allIconsByName[mapped]?.let { return it } }
    return null
}
